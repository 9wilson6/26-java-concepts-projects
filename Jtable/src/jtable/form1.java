    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String[] options = {"yes", "no"};

        int answer = JOptionPane.showOptionDialog(null, "Sure to delete??", "Delete Confirm", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE, null, options, options[1]);
        if (answer == 0) {

            int indx = jTable1.getSelectedRow();
            String id = jTable1.getValueAt(indx, 0).toString();
            if (new DBupdator().delete(id)) {
                JOptionPane.showMessageDialog(null, "succesfully Deleted");
                name_txt.setText("");
                pstn_txt.setText("");
                team_txt.setText("");
            } else {
                JOptionPane.showMessageDialog(null, "failed to delete");
            }

        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (new DBupdator().add(name_txt.getText(), pstn_txt.getText(), team_txt.getText())) {
            JOptionPane.showMessageDialog(null, "record successfully added");
            name_txt.setText("");
            pstn_txt.setText("");
            team_txt.setText("");

        } else {
            JOptionPane.showMessageDialog(null, "record not added");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int indx = jTable1.getSelectedRow();
        String id = jTable1.getValueAt(indx, 0).toString();
        if (new DBupdator().update(id, name_txt.getText(), pstn_txt.getText(), team_txt.getText())) {
            JOptionPane.showMessageDialog(null, "succesfully updated");
            name_txt.setText("");
            pstn_txt.setText("");
            team_txt.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "failed to udate");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

